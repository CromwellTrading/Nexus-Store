// Middleware de administrador con logs detallados
const isAdmin = (req, res, next) => {
  console.log("\nüîê MIDDLEWARE ADMIN - Verificando acceso de administrador...");
  
  // 1. Intentar obtener de headers
  let telegramId = req.headers['telegram-id'];
  console.log(`   üì± Telegram ID de headers: ${telegramId}`);
  
  // 2. Si no est√° en headers, intentar de query params
  if (!telegramId && req.query.tgid) {
    telegramId = req.query.tgid;
    console.log(`   üîç Telegram ID de query params: ${telegramId}`);
  }
  
  // 3. Si a√∫n no, intentar de body (para solicitudes POST)
  if (!telegramId && req.body.telegramId) {
    telegramId = req.body.telegramId;
    console.log(`   üì¶ Telegram ID de body: ${telegramId}`);
  }
  
  const adminIds = process.env.ADMIN_IDS 
    ? process.env.ADMIN_IDS.split(',').map(id => id.trim())
    : [];
  
  console.log(`   üëë IDs de administrador: ${adminIds.join(', ')}`);
  
  if (!telegramId) {
    console.log('   ‚ùå Acceso denegado: No se encontr√≥ Telegram-ID');
    return res.status(401).json({ 
      error: 'Se requiere Telegram-ID',
      instructions: 'Agregue el par√°metro ?tgid=SU_ID en la URL o env√≠e en headers'
    });
  }
  
  if (!adminIds.includes(telegramId.toString())) {
    console.log('   ‚ùå Acceso denegado: ID no autorizado');
    return res.status(403).json({ 
      error: 'Acceso no autorizado',
      yourId: telegramId,
      adminIds: adminIds
    });
  }
  
  console.log('   ‚úÖ Acceso de administrador autorizado');
  // Adjuntar el ID a la solicitud para uso posterior
  req.telegramId = telegramId;
  next();
};

